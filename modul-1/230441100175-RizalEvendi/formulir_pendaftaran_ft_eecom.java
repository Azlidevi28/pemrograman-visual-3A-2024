    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        if (jTextFieldNama.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Kolom Nama wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}
if (jTextFieldNim.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Kolom Nim wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}
if (jTextFieldAlamat.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Kolom Alamat wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}
if (jTextFieldNoHp.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Kolom Nomor wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}
if (jTextAreaketerangan.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Kolom area wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

if (!jRadioButtonLakilaki.isSelected() && !jRadioButtonPerempuan.isSelected()) {
    JOptionPane.showMessageDialog(this, "Pilih jenis kelamin terlebih dahulu", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}
if (jComboBoxProdi.getSelectedIndex() == -1) {
    JOptionPane.showMessageDialog(this, "Pilih program studi terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}
if (jComboBoxFakultas.getSelectedIndex() == -1) {
    JOptionPane.showMessageDialog(this, "Pilih program studi terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

        if(jCheckBoxcentang.isSelected()){
            Hasil_data hasil_data = new Hasil_data();
            hasil_data.jTextFieldNama.setText(jTextFieldNama.getText());
            
            hasil_data.jTextFieldNim.setText(jTextFieldNim.getText());
            
            hasil_data.jTextFieldAlamat.setText(jTextFieldAlamat.getText());
            hasil_data.jTextFieldNoHp.setText(jTextFieldNoHp.getText());
            String Prodi = jComboBoxProdi.getSelectedItem().toString();
            hasil_data.jTextFieldProdi.setText(Prodi);
            String Fakultas =jComboBoxFakultas.getSelectedItem().toString();
            hasil_data.jTextFieldFakultas.setText(Fakultas);
            if (jRadioButtonLakilaki.isSelected()){
            hasil_data.jTextFieldjenis.setText("Laki-laki");
            }
            if (jRadioButtonPerempuan.isSelected()){
            hasil_data.jTextFieldjenis.setText("Perempuan");
            }
            hasil_data.jTextArea2.setText(jTextAreaketerangan.getText());
            
            hasil_data.setVisible(true);
            JOptionPane.showMessageDialog(this,"Berhasil di simpan","Sukses",JOptionPane.INFORMATION_MESSAGE);
}else{
    JOptionPane.showMessageDialog(this,"harap centang terlebih dahulu!","Eror",JOptionPane.WARNING_MESSAGE);
        
            
            
            
        }
        
    }//GEN-LAST:event_jButtonSimpanActionPerformed

