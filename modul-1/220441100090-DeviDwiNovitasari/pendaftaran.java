    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        // TODO add your handling code here:
        if (!jCheckBox1.isSelected()){
            JOptionPane.showMessageDialog(this, "Anda harus memastikan bahwa data anda sudah benar dengan mencentang kotak", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String jenkel = "",fakultas;
        if (btnPria.isSelected()){
            jenkel = "Laki-laki";
        }else if (btnWanita.isSelected()){
            jenkel = "Perempuan";
        }else {
            JOptionPane.showMessageDialog(this, "Anda harus mengisi kolom ini!!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

       
        if (CBFakultas.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Anda harus memilih fakultas", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            fakultas = CBFakultas.getSelectedItem().toString();
            if (fakultas.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Anda harus memilih fakultas", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
       

        
        String Nama = textNama.getText();
        if (Nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Anda harus mengisi Nama", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String NIM = textNIM.getText();
        if (!NIM.isEmpty()){
        } else {
            JOptionPane.showMessageDialog(this, "Anda harus menigisi NIM", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String TempatLahir = textTempatLahir.getText();
        if (TempatLahir.isEmpty()){
            JOptionPane.showMessageDialog(this, "Anda Harus Mengisi Tempat Lahir","Peringatan" , JOptionPane.WARNING_MESSAGE);
            return;
        }
        String TanggalLahir = textTanggalLahir.getText();
        if (!TanggalLahir.isEmpty()){ 
        }else{
            JOptionPane.showMessageDialog(rootPane, "Anda harus mengisi tanggal lahir", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
            
        String Alamat = textAlamat.getText();
        if (Alamat.isEmpty()){
            JOptionPane.showMessageDialog(this, "Anda Harus Mengisi Alamat","Peringatan" , JOptionPane.WARNING_MESSAGE);
            return;
        }    
        String NoHP = textNoHP.getText();
        if (!NoHP.isEmpty()){
        }else{
            JOptionPane.showMessageDialog(this, "Anda Harus Mengisi NoHP","Peringatan" , JOptionPane.WARNING_MESSAGE);
            return;
        }
        String Prodi = textProdi.getText();
        if (Prodi.isEmpty()){
            JOptionPane.showMessageDialog(this, "Anda Harus Mengisi Prodi","Peringatan" , JOptionPane.WARNING_MESSAGE);
            return;  
        }
        
        Hasil hsl = new Hasil(Nama, NIM, TempatLahir, TanggalLahir, jenkel, Alamat, NoHP, fakultas, Prodi);
        hsl.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_BtnSimpanActionPerformed

    
